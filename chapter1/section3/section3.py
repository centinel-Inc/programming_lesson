# 変数名 = 入れたいデータ
number = 1
print(number) # 1が表示される

# 「'」で文字を囲うと変数に入れることができる
# 「'」が無いと文字列は変数とみなされてしまう
text = 'apple'
print(text) # appleが表示される

# index          0         1         2       3
fruit_list = ['apple', 'orange', 'lemon', 'apple']
print(fruit_list) # ['apple', 'orange', 'lemon', 'apple']が表示される
